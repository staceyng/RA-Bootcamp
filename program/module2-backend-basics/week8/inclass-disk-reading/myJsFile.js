// Add 2 numbers and return the result
const add = (a, b) => {
  return a + b;
};

// Subtract 2 numbers and return the result
const subtract = (a, b) => {
  return a - b;
};

// Multiply 2 numbers and return the result
const multiply = (a, b) => {
  return a * b;
};

// Divide 2 numbers and return the result
const divide = (a, b) => {
  return a / b;
};
